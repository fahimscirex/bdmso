// Sentinels delimiting the build-managed Google Analytics injection.
// Keeping a fixed marker pair makes the injection idempotent (re-runs
// replace the existing block instead of duplicating it).
const GA_START = "<!-- ANALYTICS:START (generated by scripts/build.mjs - do not edit) -->";
const GA_END   = "<!-- ANALYTICS:END -->";

function buildGaSnippet(id) {
  if (!id) return "";
  // The ID itself is non-sensitive once the site is live (any visitor's
  // HTML carries it), but we keep it in .env so the value isn't pinned
  // in source-control history.
  return [
    GA_START,
    `<script async src="https://www.googletagmanager.com/gtag/js?id=${id}"></script>`,
    `<script>window.dataLayer=window.dataLayer||[];function gtag(){dataLayer.push(arguments);}gtag('js',new Date());gtag('config','${id}');</script>`,
    GA_END,
  ].join("\n");
}

// Injects (or removes, when GA_ID is blank) the GA block right before
// </head> on every HTML file in the given directory. Idempotent.
function injectAnalyticsIntoDir(targetDir) {
  const files = readdirSync(targetDir).filter((f) => f.endsWith(".html"));
  const snippet = buildGaSnippet(GA_ID);
  let touched = 0;
  for (const file of files) {
    const filePath = path.join(targetDir, file);
    let html = readFileSync(filePath, "utf8");

    const startIdx = html.indexOf(GA_START);
    const endIdx   = html.indexOf(GA_END);
    const hasBlock = startIdx !== -1 && endIdx !== -1 && endIdx > startIdx;

    if (hasBlock) {
      // Always strip the existing block first so we can either replace
      // it (GA on) or leave the file clean (GA off).
      const blockEnd = endIdx + GA_END.length;
      const before = html.slice(0, startIdx).replace(/\s*$/, "");
      const after  = html.slice(blockEnd).replace(/^\s*/, "\n");
      html = before + after;
    }

    if (snippet) {
      // Insert immediately before </head>. Pages without a </head>
      // aren't real HTML documents (post stubs etc.) - skip silently.
      const headEnd = html.search(/<\/head>/i);
      if (headEnd === -1) continue;
      html = `${html.slice(0, headEnd)}${snippet}\n${html.slice(headEnd)}`;
    }
    writeIfChanged(filePath, html);
    touched += 1;
  }
  const dirLabel = path.relative(rootDir, targetDir) || ".";
  if (snippet) console.log(`[analytics] injected GA into ${touched} page(s) in ${dirLabel}/`);
  else         console.log(`[analytics] GA_ID unset - skipped injection in ${dirLabel}/`);
  // Also handle nested directories that ship HTML (programs/, posts/).
  for (const f of readdirSync(targetDir)) {
    const sub = path.join(targetDir, f);
    if (!statSync(sub).isDirectory()) continue;
    if (f === "node_modules" || f.startsWith(".")) continue;
    const hasHtml = readdirSync(sub).some((x) => x.endsWith(".html"));
    if (hasHtml) injectAnalyticsIntoDir(sub);
  }
}

